class Course {
    constructor(courseId, departmentId, name, uniqueID, units, code, courseLevel, semester, status) {
        this.CourseId = courseId;
        this.DepartmentId = departmentId;
        this.Name = name;
        this.UniqueID = uniqueID;
        this.Units = units;
        this.Code = code;
        this.CourseLevel = courseLevel;
        this.Semester = semester;
        this.Status = status;      
    }
}